    private void btnSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection ("jdbc:mysql://localhost/gestion_paiement?characterEncoding=utf8&useSSL=false&useUnicode=true","root","");
            
            String prenom = prenomSaisi.getText();
            String nom = nomSaisi.getText();
            String email = emailSaisi.getText();
            String mpd = mpdSaisi.getText();
            
            Statement stm = con.createStatement();
//            prepareStatement pst = con.prepareStatement();
            
            String insert = "INSERT INTO users(prenom , nom , email , mpd,type) VALUES(?,?,?,?,'user')";
            pst = con.prepareStatement(insert);
            pst.setString(1,prenomSaisi.getText());
            pst.setString(2,nomSaisi.getText());
            pst.setString(3,emailSaisi.getText());
            pst.setString(4,mpdSaisi.getText());
            pst.execute() ; 
            if(pst.execute() == true){
                //si l'utilisateur cree un compte , il se redirige a la base d'authification
                dispose();
                Login connectToi = new Login();
                connectToi.show(); 
            }
     
            con.close();
            JOptionPane.showMessageDialog(null,"Enregistrement reussi");
            
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSignActionPerformed

